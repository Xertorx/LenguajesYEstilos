    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        //TODO add your handling code here:
       int fila = Tabla.getSelectedRow();
        
        Codigo.setText(Tabla.getValueAt(fila, 0).toString());
        JPaquete.setSelectedItem(Tabla.getValueAt(fila, 1).toString());
        ProvinciasDAO sqlProvincias= new  ProvinciasDAO();
        ArrayList<Provincia> listaProvincias = sqlProvincias.obtenerTodos();//Crear un array para guardar lo obtenido de la funcionDAO
        int provinciaINT = Integer.parseInt(Tabla.getValueAt(fila, 2).toString());
        for (Provincia provincia : listaProvincias) { //Encontrar similitud con la informacion recogida y el SQL mediante DAO
            int codigo = provincia.getCodigo();
            if (codigo == provinciaINT) {
                JProvincia.setSelectedItem(provincia.getNombre());
            }
        }
        //JProvincia.setSelectedItem((String) Tabla.getValueAt(fila, 2));
        Fecha.setText((String) Tabla.getValueAt(fila, 3));
        Observaciones.setText((String) Tabla.getValueAt(fila, 4));
        

    }//GEN-LAST:event_TablaMouseClicked

